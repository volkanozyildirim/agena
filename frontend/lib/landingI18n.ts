/**
 * Server-side landing-page copy for the workflow modules
 * (insights / triage / backlog). Pages read this map keyed by the
 * `?lang=` query string. Default is 'en'; unknown locales fall back to
 * English so SEO never serves a blank page.
 *
 * If you tweak any landing's copy, update all seven locales together —
 * keeping every market on the same release rhythm is the whole point.
 */
export type Lang = 'tr' | 'en' | 'es' | 'de' | 'zh' | 'it' | 'ja';
export const LANGS: Lang[] = ['tr', 'en', 'es', 'de', 'zh', 'it', 'ja'];
const FALLBACK: Lang = 'en';

export function pickLang(raw: string | string[] | undefined): Lang {
  const v = Array.isArray(raw) ? raw[0] : raw;
  if (v && (LANGS as string[]).includes(v)) return v as Lang;
  return FALLBACK;
}

